function ump45() {
    var dmg = document.getElementById('dam');
    dmg.style.width = '38' + '%';
    document.getElementById('dam_m').textContent = "전투력 : 38";
    
    var spd = document.getElementById('spd');
    spd.style.width = '65' + '%';
    document.getElementById('spd_m').textContent = "탄속 : 65";

    var tan = document.getElementById('tan');
    tan.style.width = '30' + '%';
    document.getElementById('tan_m').textContent = "장탄 수 : 30";

    var hth = document.getElementById('hth');
    hth.style.width = '57' + '%';
    document.getElementById('hth_m').textContent = "안전성 : 57";
    
    document.getElementById('smgImg').src = "./images/ump45.jpg";

    var elems = document.getElementsByClassName('container');
    for(var i = 0; i < elems.length; i+=1) {
        elems[i].style.display = 'block';
}
}

